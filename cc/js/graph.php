<script type="text/javascript">
// Flot Pie Chart with Tooltips
$(function() {

    var data = [{
        label: "Series 1",
        data: 12
    }, {
        label: "Series 2",
        data: 32
    }, {
        label: "Series 3",
        data: 9
    }, {
        label: "Series 4",
        data: 202
    }];

    var plotObj = $.plot($("#flot-pie-chart"), data, {
        series: {
            pie: {
                show: true
            }
        },
        grid: {
            hoverable: true
        },
        tooltip: true,
        tooltipOpts: {
            content: "%p.0%, %s", // show percentages, rounding to 2 decimal places
            shifts: {
                x: 20,
                y: 0
            },
            defaultTheme: false
        }
    });

});
</script>